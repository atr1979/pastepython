dia = input("informe o dia da semana: ")
if dia == "segunda":
   print("beba bastante café")
elif dia == "terca" or dia == "quarta":
   print("continue trabalhando duro")
elif dia == "quinta":
   print("calma, sexta feira está chegando")
elif dia == "sexta":
   print("uhuuuu, hoje é sexta vamos bebemorar")
else:
   print("parabéns você chegou vivo ao final de semana, não se iluda semana que vem tem mais.") 

